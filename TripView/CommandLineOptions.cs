using CommandLine;

namespace TripView
{
    public class CommandLineOptions
    {
        [Option('f', "file", Required = false, HelpText = "Trip CSV Log file to load on startup.")]
        public string Filename { get; set; } = string.Empty;

        [Option('g', "graph", Required = false, HelpText = "Graph to show by default.", Default = "SOC x Time")]
        public string Graph { get; set; } = string.Empty;
    }
}